import UIKit
import BCColor

extension UIColor {

    class func primaryColor() -> UIColor {
        return UIColor(red: 255/255.0, green: 96/255.0, blue: 48/255.0, alpha: 1.0);
    }

    class func backgroudColor() -> UIColor {
        return UIColor(red: 100/255.0, green: 100/255.0, blue: 100/255.0, alpha: 1.0);
    }

    class func navBackgroudColor() -> UIColor {
        return UIColor(red: 100/255.0, green: 100/255.0, blue: 100/255.0, alpha: 1.0);
    }
}